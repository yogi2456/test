var numbers = [1, 2, 3, 4, 5, 6, 7, 8, 45, 67, 33, 44, 68, 94, 22, 12, 46];

function findevennumbers(numbers) {
    var result = [];
    for (var i = 0; i < numbers.length; i++) {
        if (numbers[i] % 2 == 0) {
            //console.log(numbers[i])
            result.push(numbers[i])
        }
    }
    return result;
}

