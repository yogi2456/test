var strings = ["yogesh", "awdiz", "ram", "sagar", "govandi", "mahesh"];

function stringsoflength(array) {
    var result = [];
    for (var i = 0; i < array.length; i++) {
        //console.log(array[i].length)
        result.push(array[i].length)
    }
    return result;
}

